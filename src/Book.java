
public class Book
{
    private int pages;
    private String author;
 
    private int chapters;

    public Book()
{
pages=0;
chapters=0;
author="";
            
    
}
public void setpages(int p)
{
pages=p;
}
public void setchapters(int c)
{
chapters=c;
}
public void setauthor(String a)
{
author=a;
}
}